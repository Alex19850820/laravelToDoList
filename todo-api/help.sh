#!/bin/bash

echo "Доступные команды в текущем каталоге:"
echo "-------------------------------"

# Перебираем все файлы в текущем каталоге
for file in ./*; do
  # Проверяем, что это файл и он исполняемый
  if [ -f "$file" ] && [ -x "$file" ]; then
    # Получаем базовое имя файла (без пути)
    filename=$(basename "$file")
    
    # Исключаем artisan (с любым расширением или без)
    if [[ "$filename" == "artisan" || "$filename" == "artisan.sh" ]]; then
      continue  # пропускаем этот файл
    fi
    
    # Выводим имя файла в формате ./<имя>
    echo "./$filename"
  fi
done
echo "./update_task.sh"

echo "-------------------------------"
echo "Для получения справки по конкретной команде используйте:"
echo "  ./<команда> --help  или  ./<команда> -h"
